# Sorting:
# Sort the list marks = [88, 55, 78, 90, 66] in ascending and descending order.

marks = [88,55,78,90,66]
print('Original : ',marks)

# sorting in ascending order
marks.sort()
print('Acsending Order :',marks)

# sorting in decsending order
marks.sort(reverse = True)
print('Descending Order :',marks)